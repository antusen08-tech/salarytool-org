export interface TaxBracket {
  min: number;
  max: number | null;
  rate: number; // decimal e.g. 0.05
}

export interface StateData {
  name: string;
  slug: string;
  abbr: string;
  minWage: number; // 2025
  incomeTax:
    | { type: "none" }
    | { type: "flat"; rate: number }
    | { type: "graduated"; single: TaxBracket[]; married: TaxBracket[] };
  sdi?: number; // employee SDI rate (decimal)
  sdiWageCap?: number; // annual wage cap for SDI
  tagline: string; // used in page description
}

// Helper to build graduated brackets
function brackets(pairs: [number, number | null, number][]): TaxBracket[] {
  return pairs.map(([min, max, rate]) => ({ min, max, rate }));
}

